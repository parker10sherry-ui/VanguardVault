-- Create players table
CREATE TABLE IF NOT EXISTS players (
  key TEXT PRIMARY KEY,
  full_name TEXT NOT NULL,
  team TEXT NOT NULL,
  espn_id INTEGER,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- Create cards table
CREATE TABLE IF NOT EXISTS cards (
  id SERIAL PRIMARY KEY,
  year INTEGER NOT NULL,
  player_key TEXT NOT NULL REFERENCES players(key),
  product TEXT NOT NULL DEFAULT '',
  psa INTEGER NOT NULL DEFAULT 0,
  value INTEGER NOT NULL DEFAULT 0,
  pct TEXT DEFAULT '',
  range TEXT DEFAULT '',
  purchase TEXT DEFAULT '',
  cert_number TEXT,
  source TEXT DEFAULT 'seed',
  psa_verified BOOLEAN DEFAULT false,
  psa_image_url TEXT,
  psa_population INTEGER,
  psa_pop_higher INTEGER,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now()
);

-- Enable Row Level Security with open access (no auth needed)
ALTER TABLE players ENABLE ROW LEVEL SECURITY;
ALTER TABLE cards ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow all on players" ON players FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Allow all on cards" ON cards FOR ALL USING (true) WITH CHECK (true);

